const GLYPH_MAP: ReadonlyMap<string, PixelGlyph> = new Map(
  GLYPHS.map((gl) => [gl.char, gl])
);

export const getGlyph = (char: string): PixelGlyph | undefined =>
  GLYPH_MAP.get(char.toUpperCase());

export const measureLineWidth = (text: string, config: LayoutConfig): number =>
  text.length === 0
    ? 0
    : text
        .toUpperCase()
        .split("")
        .reduce((total, ch, i) => {
          const glyph = getGlyph(ch);
          const charWidth = ch === " " ? config.spaceWidth : (glyph?.width ?? 0);
          const gap = i > 0 ? config.charGap : 0;
          return total + gap + charWidth;
        }, 0);

export const layoutLines = (
  lines: ReadonlyArray<string>,
  config: LayoutConfig
): ReadonlyArray<PixelPosition> =>
  lines.flatMap((line, lineIndex) => {
    const yOffset = lineIndex * (7 + config.lineGap);
    const chars = line.toUpperCase().split("");

    interface CharAcc {
      readonly positions: ReadonlyArray<PixelPosition>;
      readonly x: number;
    }

    const result = chars.reduce<CharAcc>(
      (acc, ch, charIndex) => {
        const xStart = charIndex === 0 ? acc.x : acc.x + config.charGap;

        if (ch === " ") {
          return { positions: acc.positions, x: xStart + config.spaceWidth };
        }

        const glyph = getGlyph(ch);
        if (!glyph) {
          return { positions: acc.positions, x: xStart };
        }

        const glyphPositions = glyph.pixels.flatMap((row, rowIndex) =>
          row
            .map((cell, colIndex) =>
              cell === 1
                ? ({
                    gridX: xStart + colIndex,
                    gridY: yOffset + rowIndex,
                    sourceChar: ch,
                    charIndex,
                  } as const)
                : undefined
            )
            .filter((p): p is PixelPosition => p !== undefined)
        );

        return {
          positions: [...acc.positions, ...glyphPositions],
          x: xStart + glyph.width,
        };
      },
      { positions: [], x: 0 }
    );

    return result.positions;
  });

export const computeCenterOffset = (
  positions: ReadonlyArray<PixelPosition>,
  canvasWidth: number,
  canvasHeight: number,
  pixelSize: number
): { readonly x: number; readonly y: number } => {
  if (positions.length === 0) {
    return { x: 0, y: 0 };
  }

  const maxGridX = positions.reduce(
    (max, p) => Math.max(max, p.gridX),
    -Infinity
  );
  const maxGridY = positions.reduce(
    (max, p) => Math.max(max, p.gridY),
    -Infinity
  );

  const contentWidth = (maxGridX + 1) * pixelSize;
  const contentHeight = (maxGridY + 1) * pixelSize;

  return {
    x: Math.floor((canvasWidth - contentWidth) / 2),
    y: Math.floor((canvasHeight - contentHeight) / 2),
  };
};
