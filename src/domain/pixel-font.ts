export interface PixelGlyph {
  readonly char: string;
  readonly width: number;
  readonly height: number;
  readonly pixels: ReadonlyArray<ReadonlyArray<0 | 1>>;
}

export interface PixelPosition {
  readonly gridX: number;
  readonly gridY: number;
  readonly sourceChar: string;
  readonly charIndex: number;
}

export interface LayoutConfig {
  readonly pixelSize: number;
  readonly charGap: number;
  readonly spaceWidth: number;
  readonly lineGap: number;
}

export const createDefaultLayoutConfig = (): LayoutConfig => ({
  pixelSize: 4,
  charGap: 1,
  spaceWidth: 3,
  lineGap: 3,
});

type Row = ReadonlyArray<0 | 1>;

const g = (char: string, ...rows: Row[]): PixelGlyph => ({
  char,
  width: rows[0].length,
  height: rows.length,
  pixels: rows,
});

